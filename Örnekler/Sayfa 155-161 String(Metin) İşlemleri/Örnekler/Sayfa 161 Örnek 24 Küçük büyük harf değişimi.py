'''
upper(): Karakter dizisindeki bütün harfleri büyütür
lower(): Karakter dizisindeki bütün harfleri küçültür
capitalize(): Karakter dizisinin ilk harfini büyütür
title(): Karakter dizisindekiher kelimenin ilk harfini büyütür
swapcase(): Karakter dizisindeki büyük harfleri küçük, küçük harfleri büyük hale getirir
'''

kelime="Bilişim Teknolijileri"

print(kelime.upper())
print(kelime.lower())
print(kelime.capitalize())
print(kelime.title())
print(kelime.swapcase())
